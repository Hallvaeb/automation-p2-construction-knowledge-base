from abc import ABC, abstractmethod


class Zone(ABC):

    @abstractmethod
    def __init__():
        pass

    @abstractmethod
    def addToKB():
        pass

    @abstractmethod
    def remove():
        pass

    @abstractmethod
    def addZone():
        pass
    @abstractmethod
    def getZones():
        pass

    @abstractmethod
    def getID():
        pass

    @abstractmethod
    def getType():
        pass

    @abstractmethod
    def getHeight():
        pass

    @abstractmethod
    def getWidth():
        pass

    @abstractmethod
    def getLength():
        pass

    @abstractmethod
    def getVolume():
        pass
        

