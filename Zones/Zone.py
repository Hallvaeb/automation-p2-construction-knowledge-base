from abc import ABC, abstractmethod


class Zone(ABC):

    @abstractmethod
    def __init__():
        pass

    @abstractmethod
    def add_to_KB():
        pass

    @abstractmethod
    def remove():
        pass

    @abstractmethod
    def add_zone():
        pass
    @abstractmethod
    def get_zones():
        pass

    @abstractmethod
    def get_ID():
        pass

    @abstractmethod
    def get_type():
        pass

    @abstractmethod
    def get_height():
        pass

    @abstractmethod
    def get_width():
        pass

    @abstractmethod
    def get_length():
        pass

    @abstractmethod
    def get_volume():
        pass
        

